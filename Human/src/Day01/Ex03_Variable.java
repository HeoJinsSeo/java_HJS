package Day01;

public class Ex03_Variable {

	public static void main(String[] args) {
		int x ; // int 형 변수 x 선언
	    int y ; // int 형 변수 y 선언
	    // 한줄 복사 : ctrl + alt + ()
	     x = 10; //변수 X에 10을 대입
	     y = 20; //변수 Y에 20대입

	     //x+y
	     //피연산자 : x, y
	     //연산자  : +
	     
	     // sysout : ctrl + space
	     
	     System.out.println("x : " + x);
	     System.out.println("y : " + y);
	     System.out.println("x + y = " + (x+y) );
	     System.out.println("(x + y) / 2 = " + (x+y)/2);
	     
	     
	     
	}
	
	
	
	
}
